package lora.ns.actility.rest.model;

public class FileSystemUsage {
    private String name;
    private Integer used;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getUsed() {
        return used;
    }

    public void setUsed(Integer used) {
        this.used = used;
    }
}
